int		ft_strchrpos(const char *s, int c)
{
	int		i;
	char	c2;

	i = 0;
	c2 = (char)c;
	while (s[i] != '\0' && c2 != s[i])
		i++;
	if (s[i] != c2)
		return (-1);
	else
		return (i);
}
